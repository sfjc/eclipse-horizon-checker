# Metadata in tiles.json lists available tiles with max_h, dimensions, etc.

def load_canopy_meta(canopy_dir: Path) -> dict | None:
    """Load canopy tiles.json metadata. Returns dict or None."""
    meta_path = canopy_dir / "tiles.json"
    if not meta_path.exists():
        return None
    return json.loads(meta_path.read_text())


def build_canopy_mosaic(lat_min: float, lat_max: float,
                        lon_min: float, lon_max: float,
                        canopy_dir: Path = CANOPY_TILES_DIR,
                        ) -> tuple[np.ndarray, float, float, float, float] | None:
    """
    Build a canopy height mosaic from local pre-generated tiles.

    Returns (mosaic, lat_min, lat_max, lon_min, lon_max) where mosaic is a
    float32 array of tree heights in metres, or None if no tiles found.
    """
    from PIL import Image

    meta = load_canopy_meta(canopy_dir)
    if meta is None:
        print(f"    Canopy: no tiles.json found in {canopy_dir}", flush=True)
        return None

    # Build index of available tiles
    tile_index = {}
    for t in meta.get("tiles", []):
        tile_index[(t["lat"], t["lon"])] = t

    # Snap bounds to integer degrees (tile boundaries)
    ila_min = int(math.floor(lat_min))
    ila_max = int(math.floor(lat_max))
    ilo_min = int(math.floor(lon_min))
    ilo_max = int(math.floor(lon_max))

    # Collect tiles that overlap the requested region
    loaded = 0
    tile_arrays = {}  # (lat, lon) -> numpy array

    for la in range(ila_min, ila_max + 1):
        for lo in range(ilo_min, ilo_max + 1):
            key = (la, lo)
            if key not in tile_index:
                continue
            t = tile_index[key]
            fpath = canopy_dir / t["file"]
            if not fpath.exists():
                continue
            try:
                img = np.array(Image.open(fpath), dtype=np.float32)
                tile_arrays[key] = img
                loaded += 1
            except Exception as e:
                print(f"    Warning: could not load {fpath}: {e}", flush=True)

    if loaded == 0:
        print(f"    Canopy: no tiles found for region "
              f"lat [{ila_min},{ila_max}] lon [{ilo_min},{ilo_max}]", flush=True)
        return None

    # Build mosaic: uniform grid covering the requested bounds
    # Use the tile pixel density to set mosaic resolution
    sample_key = next(iter(tile_arrays))
    sample_arr = tile_arrays[sample_key]
    px_per_deg_lat = sample_arr.shape[0]  # rows per 1° tile
    px_per_deg_lon = sample_arr.shape[1]  # cols per 1° tile

    n_deg_lat = ila_max - ila_min + 1
    n_deg_lon = ilo_max - ilo_min + 1
    mos_rows = n_deg_lat * px_per_deg_lat
    mos_cols = n_deg_lon * px_per_deg_lon
    mosaic = np.zeros((mos_rows, mos_cols), dtype=np.float32)

    for (la, lo), arr in tile_arrays.items():
        # Row 0 of mosaic = northernmost, so highest lat goes first
        r_off = (ila_max - la) * px_per_deg_lat
        c_off = (lo - ilo_min) * px_per_deg_lon

        # Handle tiles with slightly different dimensions
        h = min(arr.shape[0], mos_rows - r_off)
        w = min(arr.shape[1], mos_cols - c_off)
        if h > 0 and w > 0:
            # Tile images have row 0 = north (top), matching mosaic convention
            mosaic[r_off:r_off + h, c_off:c_off + w] = arr[:h, :w]

    actual_lat_min = float(ila_min)
    actual_lat_max = float(ila_max + 1)
    actual_lon_min = float(ilo_min)
    actual_lon_max = float(ilo_max + 1)

    max_h = mosaic.max()
    print(f"    Canopy: loaded {loaded} tiles, "
          f"{mos_rows}×{mos_cols} px, max height {max_h:.0f}m", flush=True)

    return mosaic, actual_lat_min, actual_lat_max, actual_lon_min, actual_lon_max


def sample_canopy(canopy_mosaic: np.ndarray,
                  lats: np.ndarray, lons: np.ndarray,
                  c_lat_min: float, c_lat_max: float,
                  c_lon_min: float, c_lon_max: float) -> np.ndarray:
    """Sample canopy heights for arrays of lat/lon. Returns heights in metres."""
    n_rows, n_cols = canopy_mosaic.shape
    # Row 0 = north = lat_max
    rows = (c_lat_max - lats) / (c_lat_max - c_lat_min) * (n_rows - 1)
    cols = (lons - c_lon_min) / (c_lon_max - c_lon_min) * (n_cols - 1)

    r = np.clip(np.round(rows).astype(np.int32), 0, n_rows - 1)
    c = np.clip(np.round(cols).astype(np.int32), 0, n_cols - 1)

    return canopy_mosaic[r, c]

