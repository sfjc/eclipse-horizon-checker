# Point-in-polygon for eclipse path
# ---------------------------------------------------------------------------
_eclipse_poly: list[tuple[float, float]] | None = None


def _get_eclipse_poly() -> list[tuple[float, float]]:
    global _eclipse_poly
    if _eclipse_poly is None:
        north = [(r["n_lat"], r["n_lon"]) for r in ECLIPSE_PATH_TABLE]
        south = [(r["s_lat"], r["s_lon"]) for r in ECLIPSE_PATH_TABLE]
        _eclipse_poly = north + south[::-1]
    return _eclipse_poly


def point_in_path(lat: float, lon: float) -> bool:
    poly = _get_eclipse_poly()
    inside = False
    n = len(poly)
    j = n - 1
    for i in range(n):
        yi, xi = poly[i]
        yj, xj = poly[j]
        if ((yi > lat) != (yj > lat)) and \
           (lon < (xj - xi) * (lat - yi) / (yj - yi) + xi):
            inside = not inside
        j = i
    return inside


def points_in_path_vec(lats: np.ndarray, lons: np.ndarray) -> np.ndarray:
    """Vectorized ray-casting point-in-polygon test.
    lats and lons are 1D arrays. Returns bool array."""
    poly = _get_eclipse_poly()
    n = len(poly)
    inside = np.zeros(len(lats), dtype=bool)

    yj, xj = poly[-1]
    for i in range(n):
        yi, xi = poly[i]
        # Vectorized crossing test
        cond1 = (yi > lats) != (yj > lats)
        with np.errstate(divide='ignore', invalid='ignore'):
            x_cross = (xj - xi) * (lats - yi) / (yj - yi) + xi
        cond2 = lons < x_cross
        inside ^= (cond1 & cond2)
        yj, xj = yi, xi

    return inside


def tile_overlaps_path(lat: int, lon: int) -> bool:
    """Check if any corner or center of a 1° tile falls within the path."""
    points = [
        (lat + 0.5, lon + 0.5),  # center
        (lat, lon), (lat + 1, lon), (lat, lon + 1), (lat + 1, lon + 1),  # corners
        (lat + 0.5, lon), (lat + 0.5, lon + 1),  # edge midpoints
        (lat, lon + 0.5), (lat + 1, lon + 0.5),
    ]
    return any(point_in_path(la, lo) for la, lo in points)


# ---------------------------------------------------------------------------
# Eclipse circumstances (interpolation from path table)
# ---------------------------------------------------------------------------
def _pt_to_seg_dist(px: float, py: float,
                    ax: float, ay: float,
                    bx: float, by: float) -> tuple[float, float]:
    mid_lat = math.radians((ay + by + py) / 3)
    k_lat = 111.32
    k_lon = 111.32 * math.cos(mid_lat)
    pxk, pyk = px * k_lon, py * k_lat
    axk, ayk = ax * k_lon, ay * k_lat
    bxk, byk = bx * k_lon, by * k_lat
    dx, dy = bxk - axk, byk - ayk
    s2 = dx * dx + dy * dy
    if s2 < 1e-12:
        return math.hypot(pxk - axk, pyk - ayk), 0.0
    t = max(0.0, min(1.0, ((pxk - axk) * dx + (pyk - ayk) * dy) / s2))
    return math.hypot(pxk - (axk + t * dx), pyk - (ayk + t * dy)), t


def get_sun_position(lat: float, lon: float) -> tuple[float, float]:
    """Return (sun_altitude_deg, sun_azimuth_deg) during totality for a location."""
    table = ECLIPSE_PATH_TABLE
    best_dist = float("inf")
    best_i = 0
    best_t = 0.0
    for i in range(len(table) - 1):
        a, b = table[i], table[i + 1]
        d, t = _pt_to_seg_dist(lon, lat, a["c_lon"], a["c_lat"],
                                b["c_lon"], b["c_lat"])
        if d < best_dist:
            best_dist, best_i, best_t = d, i, t

    a, b = table[best_i], table[best_i + 1]
    lerp = lambda k: a[k] + best_t * (b[k] - a[k])

    c_lat = lerp("c_lat")
    sun_alt = lerp("alt") + (lat - c_lat) * 0.15
    sun_az = lerp("az")
    return max(0.5, sun_alt), sun_az


def get_sun_positions_vec(lats: np.ndarray, lons: np.ndarray) -> tuple[np.ndarray, np.ndarray]:
    """Vectorized sun position for arrays of lat/lon.
    Returns (altitudes, azimuths) arrays."""
    table = ECLIPSE_PATH_TABLE
    n_seg = len(table) - 1
    n_pts = len(lats)

    # For each point, find nearest centerline segment
    best_dist = np.full(n_pts, np.inf)
    best_i = np.zeros(n_pts, dtype=np.int32)
    best_t = np.zeros(n_pts)

    for i in range(n_seg):
        a, b = table[i], table[i + 1]
        # Vectorized point-to-segment distance
        mid_lat = np.radians((a["c_lat"] + b["c_lat"] + lats) / 3)
        k_lat = 111.32
        k_lon = 111.32 * np.cos(mid_lat)

        pxk = lons * k_lon
        pyk = lats * k_lat
        axk = a["c_lon"] * k_lon
        ayk = a["c_lat"] * k_lat
        bxk = b["c_lon"] * k_lon
        byk = b["c_lat"] * k_lat

        dx = bxk - axk
        dy = byk - ayk
        s2 = dx * dx + dy * dy
        s2 = np.where(s2 < 1e-12, 1e-12, s2)
        t = np.clip(((pxk - axk) * dx + (pyk - ayk) * dy) / s2, 0, 1)
        d = np.hypot(pxk - (axk + t * dx), pyk - (ayk + t * dy))

        mask = d < best_dist
        best_dist = np.where(mask, d, best_dist)
        best_i = np.where(mask, i, best_i)
        best_t = np.where(mask, t, best_t)

    # Interpolate sun position for each point
    sun_alts = np.zeros(n_pts)
    sun_azs = np.zeros(n_pts)

    for i in range(n_seg):
        seg_mask = best_i == i
        if not seg_mask.any():
            continue
        a, b = table[i], table[i + 1]
        t = best_t[seg_mask]
        c_lat = a["c_lat"] + t * (b["c_lat"] - a["c_lat"])
        alt = a["alt"] + t * (b["alt"] - a["alt"])
        az = a["az"] + t * (b["az"] - a["az"])
        sun_alts[seg_mask] = np.maximum(0.5, alt + (lats[seg_mask] - c_lat) * 0.15)
        sun_azs[seg_mask] = az

    return sun_alts, sun_azs


