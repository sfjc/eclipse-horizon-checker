def tile_filename(lat: int, lon: int) -> str:
    ns = "N" if lat >= 0 else "S"
    ew = "E" if lon >= 0 else "W"
    return f"{ns}{abs(lat):02d}{ew}{abs(lon):03d}.hgt"


def download_tile(lat: int, lon: int, cache_dir: Path) -> Path:
    cache_dir.mkdir(parents=True, exist_ok=True)
    fname = tile_filename(lat, lon)
    local_path = cache_dir / fname

    if local_path.exists() and local_path.stat().st_size > 1_000_000:
        return local_path

    ns = "N" if lat >= 0 else "S"
    tile_name = f"{ns}{abs(lat):02d}{'E' if lon >= 0 else 'W'}{abs(lon):03d}"
    urls = [
        f"https://opentopography.s3.sdsc.edu/raster/SRTM_GL1/SRTM_GL1_srtm/{tile_name}.hgt",
        f"https://s3.amazonaws.com/elevation-tiles-prod/skadi/{ns}{abs(lat):02d}/{tile_name}.hgt.gz",
    ]

    for url in urls:
        print(f"    Downloading {fname} ...", end=" ", flush=True)
        try:
            r = requests.get(url, timeout=120, stream=True)
            if r.status_code != 200:
                print(f"HTTP {r.status_code}")
                continue
            data = r.content
            if url.endswith(".gz"):
                data = gzip.decompress(data)
            local_path.write_bytes(data)
            print(f"OK ({len(data) / 1e6:.1f} MB)")
            return local_path
        except Exception as e:
            print(f"failed ({e})")

    raise FileNotFoundError(f"Could not download {fname}")


def load_srtm_tile(lat: int, lon: int, cache_dir: Path) -> np.ndarray | None:
    """Load a single SRTM tile, returns 3601×3601 int16 array or None."""
    try:
        path = download_tile(lat, lon, cache_dir)
        data = np.frombuffer(path.read_bytes(), dtype=">i2")
        side = int(math.sqrt(len(data)))
        return data.reshape((side, side))
    except (FileNotFoundError, ValueError):
        return None


def build_mosaic(lat_min: int, lat_max: int,
                 lon_min: int, lon_max: int,
                 cache_dir: Path) -> tuple[np.ndarray, int, int, int]:
    """
    Build a contiguous elevation mosaic from SRTM tiles.

    Returns (mosaic, lat_min, lon_min, tile_side) where mosaic is a float32
    array covering [lat_min, lat_max+1] × [lon_min, lon_max+1].
    """
    # Determine tile resolution from first available tile
    tile_side = 3601  # SRTM1 default
    n_lat = lat_max - lat_min + 1
    n_lon = lon_max - lon_min + 1
    rows = n_lat * (tile_side - 1) + 1
    cols = n_lon * (tile_side - 1) + 1

    mosaic = np.zeros((rows, cols), dtype=np.float32)

    for la in range(lat_min, lat_max + 1):
        for lo in range(lon_min, lon_max + 1):
            tile = load_srtm_tile(la, lo, cache_dir)
            if tile is None:
                continue
            tile_side = tile.shape[0]
            # Position in mosaic: top-left corner
            # SRTM rows go from north to south (top = lat+1, bottom = lat)
            r_off = (lat_max - la) * (tile_side - 1)
            c_off = (lo - lon_min) * (tile_side - 1)
            h = tile.shape[0]
            w = tile.shape[1]
            mosaic[r_off:r_off + h, c_off:c_off + w] = tile.astype(np.float32)

    # Replace voids
    mosaic[mosaic < -500] = 0.0

    return mosaic, lat_min, lon_min, tile_side


def sample_mosaic(mosaic: np.ndarray,
                  lats: np.ndarray, lons: np.ndarray,
                  mos_lat_min: int, mos_lon_min: int,
                  mos_lat_max: int, tile_side: int) -> np.ndarray:
    """
    Bilinear-interpolated elevation lookup for arrays of lat/lon.
    Returns elevations in metres.
    """
    n = tile_side - 1  # pixels per degree

    # Convert to mosaic pixel coordinates
    # Row 0 = top = lat_max + 1, increasing row = decreasing lat
    rows = (mos_lat_max + 1 - lats) * n
    cols = (lons - mos_lon_min) * n

    r0 = np.clip(np.floor(rows).astype(np.int32), 0, mosaic.shape[0] - 2)
    c0 = np.clip(np.floor(cols).astype(np.int32), 0, mosaic.shape[1] - 2)
    dr = np.clip(rows - r0, 0, 1)
    dc = np.clip(cols - c0, 0, 1)

    z00 = mosaic[r0, c0]
    z01 = mosaic[r0, c0 + 1]
    z10 = mosaic[r0 + 1, c0]
    z11 = mosaic[r0 + 1, c0 + 1]

    return z00 * (1 - dr) * (1 - dc) + z01 * (1 - dr) * dc + \
           z10 * dr * (1 - dc) + z11 * dr * dc


# ---------------------------------------------------------------------------
# Vectorized ray-march for clearance computation
# ---------------------------------------------------------------------------
def compute_clearance_tile(tile_lat: int, tile_lon: int,
                           resolution_m: float = 250.0,
                           max_ray_m: float = 50_000.0,
                           ray_step_m: float = 90.0,
                           observer_h: float = 2.0,
                           cache_dir: Path = SRTM_CACHE,
                           use_canopy: bool = True,
                           canopy_dir: Path = CANOPY_TILES_DIR,
                           ) -> np.ndarray | None:
    """
    Compute clearance margin for every grid point in a 1°×1° tile.

    When use_canopy is True, tree heights from local canopy tiles are
    added to terrain elevations along each ray, giving the effective
    horizon including canopy obstruction.

    Returns a 2D float32 array of margin values in degrees, or None if
    the tile doesn't overlap the eclipse path.
    """
    if not tile_overlaps_path(tile_lat, tile_lon):
        return None

    # Grid dimensions
    lat_span_m = 111_320.0  # metres per degree latitude
    lon_span_m = 111_320.0 * math.cos(math.radians(tile_lat + 0.5))

    n_rows = max(2, int(lat_span_m / resolution_m))
    n_cols = max(2, int(lon_span_m / resolution_m))

    # Grid point coordinates (cell centres)
    dlat = 1.0 / n_rows
    dlon = 1.0 / n_cols
    lats_1d = np.linspace(tile_lat + dlat / 2, tile_lat + 1 - dlat / 2, n_rows)
    lons_1d = np.linspace(tile_lon + dlon / 2, tile_lon + 1 - dlon / 2, n_cols)
    # lats_1d goes south to north; we'll flip at the end for image convention
    grid_lons, grid_lats = np.meshgrid(lons_1d, lats_1d)  # shape (n_rows, n_cols)

    # Vectorized in-path mask
    flat_all_lats = grid_lats.ravel()
    flat_all_lons = grid_lons.ravel()
    in_path_flat = points_in_path_vec(flat_all_lats, flat_all_lons)
    in_path = in_path_flat.reshape(n_rows, n_cols)

    n_active = in_path.sum()
    if n_active == 0:
        return None

    print(f"  Grid: {n_rows}×{n_cols} = {n_rows * n_cols} pts, "
          f"{n_active} inside path", flush=True)

    # Vectorized sun positions for active points
    flat_lats = grid_lats[in_path]
    flat_lons = grid_lons[in_path]
    sun_alts_flat, sun_azs_flat = get_sun_positions_vec(flat_lats, flat_lons)

    # Use median azimuth for ray direction (varies <2° across a tile)
    sun_az_median = float(np.median(sun_azs_flat))
    az_rad = math.radians(sun_az_median)

    # Load SRTM mosaic with buffer for rays
    buf_deg = max_ray_m / 111_320.0 + 0.5
    mos_lat_min = int(math.floor(tile_lat - buf_deg))
    mos_lat_max = int(math.floor(tile_lat + 1 + buf_deg))
    mos_lon_min = int(math.floor(tile_lon - buf_deg))
    mos_lon_max = int(math.floor(tile_lon + 1 + buf_deg))

    print(f"  Loading SRTM mosaic: lat [{mos_lat_min}, {mos_lat_max}], "
          f"lon [{mos_lon_min}, {mos_lon_max}] ...", flush=True)
    mosaic, _, _, tile_side = build_mosaic(
        mos_lat_min, mos_lat_max, mos_lon_min, mos_lon_max, cache_dir)

    # Load canopy mosaic (covers same extent as SRTM buffer)
    canopy_data = None
    if use_canopy:
        print(f"  Loading canopy data ...", flush=True)
        canopy_result = build_canopy_mosaic(
            mos_lat_min, mos_lat_max + 1,
            mos_lon_min, mos_lon_max + 1,
            canopy_dir=canopy_dir,
        )
        if canopy_result is not None:
            canopy_data = canopy_result

    # Observer elevations (flat_lats, flat_lons already defined above)
    obs_elevs = sample_mosaic(mosaic, flat_lats, flat_lons,
                              mos_lat_min, mos_lon_min, mos_lat_max, tile_side)
    obs_elevs += observer_h

    # Ray-march: step along sun azimuth, track max elevation angle
    max_angles = np.full(n_active, -90.0, dtype=np.float32)
    n_steps = int(max_ray_m / ray_step_m)

    # Precompute lat/lon offsets per step (same for all points — same azimuth)
    cos_az = math.cos(az_rad)
    sin_az = math.sin(az_rad)
    mean_lat_rad = math.radians(tile_lat + 0.5)
    m_per_deg_lat = 111_320.0
    m_per_deg_lon = 111_320.0 * math.cos(mean_lat_rad)

    print(f"  Ray-marching {n_active} points × {n_steps} steps "
          f"(az={sun_az_median:.1f}°) ...", flush=True)
    t0 = time.time()

    for step in range(1, n_steps + 1):
        d = step * ray_step_m

        # Earth curvature + refraction correction
        curvature_m = (d * d) / (2 * EARTH_RADIUS_M * (1 - REFRACTION_K))

        # Target positions
        dlat_deg = d * cos_az / m_per_deg_lat
        dlon_deg = d * sin_az / m_per_deg_lon
        tgt_lats = flat_lats + dlat_deg
        tgt_lons = flat_lons + dlon_deg

        # Sample elevations
        tgt_elevs = sample_mosaic(mosaic, tgt_lats, tgt_lons,
                                  mos_lat_min, mos_lon_min, mos_lat_max, tile_side)

        # Add canopy heights if available
        if canopy_data is not None:
            cm, c_lat_min, c_lat_max, c_lon_min, c_lon_max = canopy_data
            tree_h = sample_canopy(cm, tgt_lats, tgt_lons,
                                   c_lat_min, c_lat_max, c_lon_min, c_lon_max)
            tgt_elevs += tree_h

        # Elevation angle from observer
        elev_diff = tgt_elevs - obs_elevs - curvature_m
        angles = np.degrees(np.arctan2(elev_diff, d))

        # Track max
        np.maximum(max_angles, angles, out=max_angles)

        if step % 100 == 0:
            print(f"\r    step {step}/{n_steps} "
                  f"({step * ray_step_m / 1000:.0f} km) ...", end="", flush=True)

    elapsed = time.time() - t0
    print(f"\r    {n_steps} steps done in {elapsed:.1f}s"
          f" ({n_active * n_steps / elapsed / 1e6:.1f}M lookups/s)    ")

    # Compute margins
    flat_margins = sun_alts_flat - max_angles

    # Sea fix: SRTM ocean = 0m elevation. Points at/below sea level are open
    # ocean with no terrain obstructions — force a large positive margin to
    # prevent false red artifacts from coastal features along the ray.
    ground_elevs = obs_elevs - observer_h  # undo observer height to get ground
    sea_mask = ground_elevs <= 1.0
    if sea_mask.any():
        flat_margins[sea_mask] = 15.0  # guaranteed clear from open ocean
        n_sea = sea_mask.sum()
        print(f"    Sea fix: {n_sea} ocean points forced visible "
              f"({n_sea / n_active * 100:.0f}%)")

    # Assemble result grid (NaN for outside-path points)
    margin_grid = np.full((n_rows, n_cols), np.nan, dtype=np.float32)
    margin_grid[in_path] = flat_margins

    # Flip vertically: row 0 = north (image convention)
    margin_grid = margin_grid[::-1, :]

    return margin_grid


def encode_margin_png(margin: np.ndarray, path: Path) -> int:
    """Encode margin array as uint8 PNG. Returns file size in bytes."""
    from PIL import Image

    # Encode: value = (margin + 12.8) * 10, clamped to [0, 255]
    encoded = np.clip(np.round((margin + 12.8) * 10), 0, 255)
    # NaN → 0 (transparent in overlay)
    encoded = np.where(np.isnan(margin), 0, encoded).astype(np.uint8)

    img = Image.fromarray(encoded, mode="L")
    img.save(path, optimize=True)
    return path.stat().st_size


def generate_overview(full_path: Path, overview_path: Path,
                      scale: int = 8) -> int:
    """Downsample a clearance PNG by `scale` factor for fast map preview.
    Returns file size in bytes."""
    from PIL import Image

    img = Image.open(full_path)
    new_w = max(1, img.width // scale)
    new_h = max(1, img.height // scale)
    # Use NEAREST to preserve encoded margin values (not interpolate)
    small = img.resize((new_w, new_h), Image.NEAREST)
    small.save(overview_path, optimize=True)
    return overview_path.stat().st_size


def _process_tile(lat_lon: tuple[int, int], kwargs: dict) -> dict | None:
    """Process a single clearance tile. Module-level for multiprocessing."""
    lat, lon = lat_lon
    label = f"clearance_{lat}_{lon}"
    print(f"\n[{label}] Processing ...", flush=True)
    t0 = time.time()

    margin = compute_clearance_tile(
        lat, lon,
        resolution_m=kwargs["resolution_m"],
        max_ray_m=kwargs["max_ray_m"],
        ray_step_m=kwargs["ray_step_m"],
        observer_h=kwargs["observer_h"],
        cache_dir=kwargs["cache_dir"],
        use_canopy=kwargs["use_canopy"],
        canopy_dir=kwargs["canopy_dir"],
    )

    if margin is None:
        print(f"  → skipped (no points inside path)")
        return None

    fname = f"{label}.png"
    fpath = kwargs["output_dir"] / fname
    fsize = encode_margin_png(margin, fpath)

    # Generate low-res overview for fast map browsing
    overview_dir = kwargs["output_dir"] / "overview"
    overview_dir.mkdir(exist_ok=True)
    ov_path = overview_dir / fname
    ov_size = generate_overview(fpath, ov_path, scale=8)

    valid = margin[~np.isnan(margin)]
    tile_info = {
        "lat": lat,
        "lon": lon,
        "file": fname,
        "kb": round(fsize / 1024, 1),
        "overview_kb": round(ov_size / 1024, 1),
        "rows": int(margin.shape[0]),
        "cols": int(margin.shape[1]),
        "margin_min": round(float(valid.min()), 2),
        "margin_max": round(float(valid.max()), 2),
        "margin_mean": round(float(valid.mean()), 2),
        "pct_visible": round(float((valid > 0).sum() / len(valid) * 100), 1),
    }

    elapsed = time.time() - t0
    print(f"  → {fname} ({tile_info['kb']:.0f} KB, "
          f"{tile_info['rows']}×{tile_info['cols']}, "
          f"{elapsed:.1f}s)")
    print(f"    margin: [{tile_info['margin_min']:+.1f}°, "
          f"{tile_info['margin_max']:+.1f}°], "
          f"mean {tile_info['margin_mean']:+.1f}°, "
          f"{tile_info['pct_visible']:.0f}% visible")
    return tile_info


