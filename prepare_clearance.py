def main() -> None:
    parser = argparse.ArgumentParser(
        description="Pre-compute eclipse sun clearance raster tiles",
    )
    parser.add_argument("--resolution", type=float, default=250.0,
                        help="Grid spacing in metres (default 250)")
    parser.add_argument("--max-ray-km", type=float, default=50.0,
                        help="Max ray-march distance in km (default 50)")
    parser.add_argument("--ray-step-m", type=float, default=90.0,
                        help="Ray step size in metres (default 90)")
    parser.add_argument("--observer-height", type=float, default=2.0,
                        help="Observer height above ground in metres (default 2)")
    parser.add_argument("--output-dir", type=str, default="clearance_tiles",
                        help="Output directory (default clearance_tiles)")
    parser.add_argument("--srtm-cache", type=str, default="srtm_cache",
                        help="SRTM cache directory (default srtm_cache)")
    parser.add_argument("--lat-min", type=int, default=37,
                        help="Minimum latitude (default 37)")
    parser.add_argument("--lat-max", type=int, default=44,
                        help="Maximum latitude (default 44)")
    parser.add_argument("--lon-min", type=int, default=-10,
                        help="Minimum longitude (default -10)")
    parser.add_argument("--lon-max", type=int, default=7,
                        help="Maximum longitude (default 7)")
    parser.add_argument("--jobs", "-j", type=int, default=1,
                        help="Parallel tile jobs (default 1, try num CPU cores)")
    parser.add_argument("--no-canopy", action="store_true",
                        help="Disable tree canopy (terrain only)")
    parser.add_argument("--canopy-dir", type=str, default="canopy_tiles",
                        help="Canopy tiles directory (default canopy_tiles)")

    args = parser.parse_args()
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    cache_dir = Path(args.srtm_cache)
    canopy_dir = Path(args.canopy_dir)

    # Check canopy availability
    canopy_meta = load_canopy_meta(canopy_dir) if not args.no_canopy else None
    use_canopy = canopy_meta is not None and not args.no_canopy
    if use_canopy:
        n_tiles = len(canopy_meta.get("tiles", []))
        canopy_label = f"terrain+canopy ({n_tiles} tiles from {canopy_dir})"
    elif args.no_canopy:
        canopy_label = "terrain only (disabled)"
    else:
        canopy_label = f"terrain only (no tiles.json in {canopy_dir})"

    print("=" * 70)
    print("  ECLIPSE CLEARANCE RASTER GENERATOR")
    print(f"  Resolution: {args.resolution:.0f}m | "
          f"Ray: {args.max_ray_km:.0f}km @ {args.ray_step_m:.0f}m steps | "
          f"Observer: {args.observer_height:.1f}m")
    print(f"  Bounds: lat [{args.lat_min}, {args.lat_max}], "
          f"lon [{args.lon_min}, {args.lon_max}] | "
          f"Jobs: {args.jobs}")
    print(f"  Canopy: {canopy_label}")
    print(f"  Output: {output_dir}/")
    print("=" * 70)

    # Build list of tiles to process
    tile_jobs = []
    for lat in range(args.lat_min, args.lat_max + 1):
        for lon in range(args.lon_min, args.lon_max + 1):
            if tile_overlaps_path(lat, lon):
                tile_jobs.append((lat, lon))

    print(f"\n  {len(tile_jobs)} tiles overlap the path")

    tile_list = []
    total_t0 = time.time()

    # Build job arguments
    job_kwargs = {
        "resolution_m": args.resolution,
        "max_ray_m": args.max_ray_km * 1000,
        "ray_step_m": args.ray_step_m,
        "observer_h": args.observer_height,
        "cache_dir": cache_dir,
        "use_canopy": use_canopy,
        "canopy_dir": canopy_dir,
        "output_dir": output_dir,
    }

    if args.jobs > 1:
        from concurrent.futures import ProcessPoolExecutor, as_completed
        print(f"  Using {args.jobs} parallel workers")
        with ProcessPoolExecutor(max_workers=args.jobs) as pool:
            futures = {pool.submit(_process_tile, job, job_kwargs): job
                       for job in tile_jobs}
            for future in as_completed(futures):
                result = future.result()
                if result is not None:
                    tile_list.append(result)
    else:
        for job in tile_jobs:
            result = _process_tile(job, job_kwargs)
            if result is not None:
                tile_list.append(result)

    # Write metadata
    meta = {
        "resolution_m": args.resolution,
        "max_ray_km": args.max_ray_km,
        "ray_step_m": args.ray_step_m,
        "observer_height_m": args.observer_height,
        "includes_canopy": use_canopy,
        "has_overview": True,
        "overview_scale": 8,
        "overview_dir": "overview",
        "encoding": "uint8, value = (margin_deg + 12.8) * 10, 128 = 0°",
        "source": "SRTM GL1 30m" + (" + Meta/WRI Canopy Height" if use_canopy else "")
                  + " + NASA/Espenak eclipse path",
        "tiles": tile_list,
    }
    meta_path = output_dir / "clearance.json"
    meta_path.write_text(json.dumps(meta, indent=2))

    total_elapsed = time.time() - total_t0
    print(f"\n{'=' * 70}")
    print(f"  Done! {len(tile_list)} tiles in {total_elapsed / 60:.1f} minutes")
    print(f"  Metadata: {meta_path}")
    print(f"{'=' * 70}")


if __name__ == "__main__":
    main()
