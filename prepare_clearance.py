#!/usr/bin/env python3
"""
prepare_clearance.py — Pre-compute eclipse sun clearance raster.

For each grid point within the totality path, computes the terrain horizon
angle in the sun's direction and the resulting clearance margin (sun_alt
minus horizon_angle).  Output as tiled PNGs suitable for web map overlay.

ENCODING
--------
Grayscale uint8 PNG, one pixel per grid cell.
  pixel_value = clamp(round((margin_deg + 12.8) * 10), 0, 255)
  128 = exactly 0° clearance (sun touches horizon)
  > 128 = sun clears horizon (green in overlay)
  < 128 = sun blocked (red in overlay)
  Resolution: 0.1° per step, range -12.8° to +12.7°

USAGE
-----
    python prepare_clearance.py                        # default: terrain + canopy
    python prepare_clearance.py --resolution 50 -j 4   # 50m grid, 4 parallel workers
    python prepare_clearance.py --no-canopy             # terrain only
    python prepare_clearance.py --canopy-dir path/to/canopy_tiles
    python prepare_clearance.py --output-dir clearance_tiles

CANOPY DATA
-----------
Reads pre-generated canopy tiles from canopy_tiles/ directory (produced by
prepare_canopy.py).  These are grayscale uint8 PNGs where pixel = tree height
in metres.  No rasterio or network access needed.  Use --no-canopy to skip.

Eclipse Predictions by Fred Espenak, NASA's GSFC.
"""

